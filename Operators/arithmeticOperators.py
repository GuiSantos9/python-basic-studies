#operadores aritméticos
# + Adição: x+y
# - Subtração: x-y
# * Multiplicação: x * y
# /   Divisão: x / y
# %   Módulo:  x % y (Retorna o resto da divisão)
# ** Exponenciação: x ** y (x elevado a y)
# //  Divisão inteira: x // y (Divide e arredonda para baixo para o número inteiro mais próximo)

#Exemplo:
x = 15
y = 4

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)
print(x ** y)
print(x // y)