# Operadores de Comparação
# Usados para comparar dois valores:

# ==  Igual a:                  x == y
# !=  Não é igual a (Diferente): x != y
# >   Maior que:                x > y
# <   Menor que:                x < y
# >=  Maior ou igual a:         x >= y
# <=  Menor ou igual a:         x <= y

x = 5
y = 3

print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)

#Comparações encadeadas
x = 5

print(1 < x < 10)

print(1 < x and x < 10)
