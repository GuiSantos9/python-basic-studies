#A função print() é frequentemente usada para variaveis output(de saída)
x = "Jesus é Bom"
print(x)

#Na função você pode printar multiplas variaveis separadas por vírgula
x = "Jesus "
y = "é "
z = "Bom"
print(x, y, z)

#Você também pode usar o operador + para exibir várias variáveis
x = "Python "
y = "is "
z = "awesome"
print(x + y + z)

#Para os números o operador + funciona como um operador matemático
x = 5
y = 10
print(x + y)

#A melhor maneira de exibir várias variáveis ​​na função print() é separá-las 
# com vírgulas, que inclusive suportam diferentes tipos de dados:
x = 5
y = "John"
