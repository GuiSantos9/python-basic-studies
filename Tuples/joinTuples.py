#Para unirmos duas ou mais tuplas utilizamos o operador +
tupla1 = ("guilherme", "jose", "thiago")
tupla2 = ("pedro", "genilda", "oswaldo")
tupla3 = tupla1 + tupla2

print(tupla3)

#Se quisermos multiplicar os conteúdos de uma tupla por um numero determinado de 
#vezes, podemos usar o operador *
fruits = ("apple", "banana", "cherry")
minhaTupla = fruits * 2

print(minhaTupla)