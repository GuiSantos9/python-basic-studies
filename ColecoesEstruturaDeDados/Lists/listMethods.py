#O Python tem alguns métodos integrados para trabalharmos com listas 

#append() -> adiciona elementos no final da lista
#clear() -> limpa os elementos da lista, porém a lista ainda existe 
#copy() -> copia os elementos de uma lista
#count() -> retorna a quantidade de elementos que foram especificados dentro de uma lista 
#extend() -> adiciona elementos de uma lista para o final de outra lista 
#index() -> retorna o indice do primeiro elemento com o valor especificado
#insert() -> adiciona uma elemento em uma posição específica
#pop() -> remove um elemento com a posição especificada
#remove() -> remove um elemento que o valor foi especificado
#reverse() -> inverte a ordem de uma lista 
#sort() -> ordena uma lista 

