#keys()	Retorna uma lista com as chaves do dicionario
#pop()	Remove o elemento com a chave especificada
#popitem()	Remove o ultimo valor-chave que foi inserido
#setdefault()	Retorna o valor de uma chave específica. Se a chave não existe: insere a chave com o valor especifico
#update()	Atualiza o dicionario com o par valor-chave especificado
#values()	Retorna uma lista com todos os valores de um dicionario


